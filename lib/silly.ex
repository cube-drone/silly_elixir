  defp number_of_things do
    tuple_size @things
  end

  def things do 
    @things
  end

  @doc """
  Pull a random thing out of the set

  ## Examples

  Test that we get a word from the list of words.

      iex> Enum.member?(Tuple.to_list(Silly.things), Silly.random_thing())
      true

  """
  def random_thing do
    elem(things(), :rand.uniform(number_of_things()-1))
  end
  
  defp number_of_names do
    tuple_size @names
  end

  @doc """
  Get a tuple containing all the names
  """
  def names do 
    @names
  end

  @doc """
  Pull a random name out of the set

  ## Examples

  Test that we get a word from the list of words.

      iex> Enum.member?(Tuple.to_list(Silly.names), Silly.random_name())
      true

  """
  def random_name do
    elem(names(), :rand.uniform(number_of_names()-1))
  end

  @doc """
  return true or false, at about equal probability

  100 coin flips should contain both a true AND a false value

      iex> Enum.member?(Enum.map(1..100, fn _ -> Silly.coin_toss() end), true)
      true
      iex> Enum.member?(Enum.map(1..100, fn _ -> Silly.coin_toss() end), false)
      true

  """
  def coin_toss do
    :rand.uniform(2) == 2
  end

  @doc """
  Just grab a word, at random
  """
  def random_word do 
    if coin_toss(), do: random_name(), else: random_thing() 
  end

  @doc """
  Generate a random name, like "chastity truckagile"
  """
  def name do
    "#{random_name()} #{random_thing()}#{random_thing()}"
  end

  @doc """
  Generate a random slug, like "chastity-truckagile"
  """
  def slug do
    String.replace(name(), " ", "-")
  end
  
  @doc """
  A tuple containing both letters AND numbers
  """
  def alphanums do 
    @alphanum
  end
  
  defp number_of_alphanums do
    tuple_size @alphanum
  end

  @doc """
  You know, like "a", or "S", or "7"
  """
  def random_alphanum do
    elem(alphanums(), :rand.uniform(number_of_alphanums()-1))
  end

  @doc """
  a word and three alphanumeric characters, like "spud-92D" or "fart-8dF"
  """
  def short do
    short_str = "#{random_word()} #{random_alphanum()}#{random_alphanum()}#{random_alphanum()}"
    String.replace(short_str, " ", "-")
  end

  @doc """
  a name and four alphanumeric characters, like "grizzle-beefshanks-A2dD"
  """
  def long do
    long_str = "#{name()} #{random_alphanum()}#{random_alphanum()}#{random_alphanum()}#{random_alphanum()}"
    String.replace(long_str, " ", "-")
  end

  def tld do
    elem(@tld, :rand.uniform(tuple_size(@tld) - 1))
  end

  @doc """
  a domain, like "socks.edu" or "mustard.gov"
  """
  def domain do
    if coin_toss() do 
      "#{String.replace(random_word(), " ", "-")}.#{tld()}"
    else
      "#{String.replace(random_word(), " ", "-")}.#{String.replace(random_word(), " ", "-")}.#{tld()}"
    end
  end

  def email do
    "#{long()}@#{domain()}"
  end
